     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $seminar= new Seminar();
        $seminar->Topic=$request->input('Topic');
        $seminar->venue=$request->input('venue');
        $seminar->time_start=$request->input('time_start');
        $seminar->time_end=$request->input('time_end');
        $seminar->date=$request->input('date');
        $seminar->speaker=$request->input('speaker');
        $seminar->limit=$request->input('limit');
        $seminar->save();
        return redirect()->route('seminar.index');
    }

